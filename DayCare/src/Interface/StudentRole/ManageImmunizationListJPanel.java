    private void btnDeleteImmunizationRecordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteImmunizationRecordActionPerformed
        // TODO add your handling code here:
        int row = tableRegistrationList.getSelectedRow();

        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Immunization s = (Immunization)tableRegistrationList.getValueAt(row, 0);
        student.getImmunizationList().getImmunizationList().remove(s);
        refreshTable();
    }//GEN-LAST:event_btnDeleteImmunizationRecordActionPerformed


