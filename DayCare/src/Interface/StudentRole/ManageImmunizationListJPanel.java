    public void refreshTable() {
        DefaultTableModel dtm =(DefaultTableModel)tableRegistrationList.getModel();
         dtm.setRowCount(0);
         
         for(Immunization p1:student.getImmunizationList().getImmunizationList()){
            Object row[] = new Object[4];
            row[0] = p1;
            row[1] = p1.getTime();
            row[2] = p1.getType();
            row[3] = p1.getPeriod();
            dtm.addRow(row);
         } 
    }
