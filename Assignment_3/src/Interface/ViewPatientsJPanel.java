    private void btnAddVitalSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVitalSignActionPerformed
        // TODO add your handling code here:
          int selectedRow = tblPatientsInfo.getSelectedRow();
        if(selectedRow>=0){
            Patient pt =(Patient) tblPatientsInfo.getValueAt(selectedRow, 0);
            VitalSignHistory vsh =pt.getPatientVSH();
             AddVSJPanel addVSJPanel = new AddVSJPanel(pt,vsh,rightJPanel);
             rightJPanel.add("addVSJPanel",addVSJPanel);
             CardLayout layout = (CardLayout) rightJPanel.getLayout();
             layout.next(rightJPanel);
        }else
            JOptionPane.showMessageDialog(null, "Please select a Row !");
    }//GEN-LAST:event_btnAddVitalSignActionPerformed


