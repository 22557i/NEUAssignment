    private void btnviewPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnviewPatientActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPatientsInfo.getSelectedRow();
        if(selectedRow>=0){
            Patient pt =(Patient) tblPatientsInfo.getValueAt(selectedRow, 0);
            txtPatientsName.setText(pt.getPatientName());
            txtPatientsID.setText(pt.getPatientID());
            txtDoctorsName.setText(pt.getPrimaryDoctorName());
            txtPreferedPharamacy.setText(pt.getPrimaryDoctorName());
            
        }else
            JOptionPane.showMessageDialog(null, "Please select a Row !");
         
    }//GEN-LAST:event_btnviewPatientActionPerformed

