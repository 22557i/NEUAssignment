    private void populateTable() {
        DefaultTableModel dtm =(DefaultTableModel)tblPatientsInfo.getModel();
        dtm.setRowCount(0);
         
        for(Patient p1:pl.getPatientList()){
            Object row[] = new Object[4];
            row[0]=p1;
            row[1]=p1.getPatientID();
            row[2] = p1.getPrimaryDoctorName();
            row[3] = p1.getPreferedPharamacy();
                dtm.addRow(row);
 }
   
    }
