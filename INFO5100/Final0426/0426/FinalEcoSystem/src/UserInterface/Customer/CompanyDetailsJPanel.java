    public void populateTableReview() {
       DefaultTableModel dtm = (DefaultTableModel) tableReview.getModel();
        dtm.setRowCount(0);
        if(cargoDelivery!=null){
        for (OrderRequest or : cargoDelivery.getOrderList()) {
                Object row[] = new Object[4];
                //date
                row[0] = or.getReview();
                //customer
                row[1] = or.getAccount().getUsername();
                //score
                
                Random ra =new Random();
                row[2] =  new BigDecimal(ra.nextDouble() * 2+8).setScale(1, BigDecimal.ROUND_HALF_UP).doubleValue();
                //comment
                row[3] = or.getReviewStatus();
                dtm.addRow(row);
            }
        }else {
            for (OrderRequest or :pickupService.getOrderList()) {
                Object row[] = new Object[4];
                //date
                row[0] = or.getReview();
                //customer
                row[1] = or.getAccount().getUsername();
                //score
                 Random ra =new Random();
                row[2] =  new BigDecimal(ra.nextDouble() * 2+8).setScale(1, BigDecimal.ROUND_HALF_UP).doubleValue();
                //comment
                row[3] = or.getReviewStatus();
                dtm.addRow(row);
            }
        }
    }
    
